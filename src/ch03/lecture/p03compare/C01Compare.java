package ch03.lecture.p03compare;

public class C01Compare {
    public static void main(String[] args) {
        //비교연산자
        //같다, 같지않다, 크다, 크거나 같다, 작다, 같거다 작다.
        //== , !=, >, >=, <, <=

        //연산결과는 boolean 이다
        int a =3;
        int b= 5;
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a>b);
        System.out.println(a>=b);
        System.out.println(a<b);
        System.out.println(a<=b);
    }
}
